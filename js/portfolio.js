const app = Vue.createApp({
    data() {
        return {
            lang: 'en',
            isDarkMode: true,
            isScrolled: false,
            mobileMenuOpen: false
        }
    },
    computed: {
        t() {
            return translations[this.lang];
        },
        dir() {
            return this.lang === 'ar' ? 'rtl' : 'ltr';
        }
    },
    methods: {
        toggleLanguage() {
            this.lang = this.lang === 'ar' ? 'en' : 'ar';
            localStorage.setItem('portfolio_lang', this.lang);
            document.documentElement.lang = this.lang;
            document.documentElement.dir = this.dir;
            document.title = this.t.pageTitle;
        },
        toggleTheme() {
            this.isDarkMode = !this.isDarkMode;
            localStorage.setItem('portfolio_theme', this.isDarkMode ? 'dark' : 'light');
            if (this.isDarkMode) {
                document.body.classList.add('dark-mode');
            } else {
                document.body.classList.remove('dark-mode');
            }
        },
        handleScroll() {
            this.isScrolled = window.scrollY > 20;
        },
        initLanguage() {
            const savedLang = localStorage.getItem('portfolio_lang');
            if (savedLang) {
                this.lang = savedLang;
            } else {
                const userLang = navigator.language || navigator.userLanguage;
                this.lang = userLang.startsWith('ar') ? 'ar' : 'en';
            }
            document.documentElement.lang = this.lang;
            document.documentElement.dir = this.dir;
            document.title = this.t.pageTitle;
        },
        initTheme() {
            const savedTheme = localStorage.getItem('portfolio_theme');
            if (savedTheme) {
                this.isDarkMode = savedTheme === 'dark';
            } else {
                // Auto detect system theme
                this.isDarkMode = window.matchMedia && window.matchMedia('(prefers-color-scheme: dark)').matches;
            }
            if (this.isDarkMode) {
                document.body.classList.add('dark-mode');
            } else {
                document.body.classList.remove('dark-mode');
            }
        }
    },
    mounted() {
        this.initLanguage();
        this.initTheme();
        window.addEventListener('scroll', this.handleScroll);
        
        // Listen to system theme changes dynamically
        window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', e => {
            if (!localStorage.getItem('portfolio_theme')) {
                this.isDarkMode = e.matches;
                if (this.isDarkMode) {
                    document.body.classList.add('dark-mode');
                } else {
                    document.body.classList.remove('dark-mode');
                }
            }
        });
    },
    unmounted() {
        window.removeEventListener('scroll', this.handleScroll);
    }
});

app.mount('#app');
